vim.opt.tabstop = 4           -- set number of spaces that <Tab> represents
vim.opt.shiftwidth = 0        -- set number of spaces used for shifting and autoindent (>>) -- 0 sets shiftwidth to tabstop
vim.opt.expandtab = false     -- uses \t instead of spaces (displays as tabstop # of spaces)

vim.opt.number = true         -- show absolute line numbers
vim.opt.relativenumber = true -- show relative line numbers
vim.opt.signcolumn = 'number' -- TODO: idfk someone said to use it???

vim.g.mapleader = ' ' -- sets leader key as space
